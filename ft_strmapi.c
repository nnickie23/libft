#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		str_len;
	int		i;
	char	*res;

	if (!s || !f)
		return (NULL);
	str_len = ft_strlen(s);
	if (!(res = malloc(sizeof(char) * (str_len + 1))))
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
